let totalPassed = 0;
let totalFailed = 0;

async function runTest(testFile) {
    return new Promise((resolve) => {
        const testPath = path.join(__dirname, testFile);
        const proc = spawn('node', [testPath], {
            stdio: 'inherit',
            shell: true
        });

        proc.on('close', (code) => {
            resolve(code);
        });
    });
}

